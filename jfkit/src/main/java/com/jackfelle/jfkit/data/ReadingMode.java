package com.jackfelle.jfkit.data;

public enum ReadingMode
{
	////////////////////////////////////////////////////////////////////////////////////////////
	// region Values
	
	// Words-per-minute values are based on an article of Marc Brysbaert (2019).
	// [https://doi.org/10.1016/j.jml.2019.104047]
	
	SILENT(238),
	LOUD_SLOW(100),
	LOUD(183),
	LOUD_FAST(260);
	
	// endregion
	////////////////////////////////////////////////////////////////////////////////////////////
	// region Properties
	
	public final int wordsPerMinute;
	
	// endregion
	////////////////////////////////////////////////////////////////////////////////////////////
	// region Lifecycle
	
	ReadingMode(int wordsPerMinute) {
		this.wordsPerMinute = wordsPerMinute;
	}
	
	// endregion
	////////////////////////////////////////////////////////////////////////////////////////////
}
